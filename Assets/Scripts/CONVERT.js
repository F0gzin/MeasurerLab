function converterTabelaParaObjeto(tabelaTexto) {
  const linhas = tabelaTexto.trim().split('\n');
  const resultado = {};
  let resultadoStr = "";

  linhas.forEach(linha => {
    const [chave, valor] = linha.split('\t');
    if (chave && valor) {
      resultadoStr = resultadoStr + chave + ": " + valor + ",\n"
     // resultado[chave] = valor;
    }
  });

  return resultadoStr;
}

